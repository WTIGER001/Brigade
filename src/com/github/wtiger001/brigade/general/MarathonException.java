package com.github.wtiger001.brigade.general;

public class MarathonException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7836125436893893690L;

	public MarathonException() {
		super();
	}

	public MarathonException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public MarathonException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MarathonException(String arg0) {
		super(arg0);
	}

	public MarathonException(Throwable arg0) {
		super(arg0);
	}

}
